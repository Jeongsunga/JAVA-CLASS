package j03_연산자;

public class Operation7 {

	public static void main(String[] args) {

		int result = 10;
		System.out.println(result);
		
		result = result + 10;
		System.out.println(result);
		
		result += 10;
		System.out.println(result);
		
		result /= 2;
		System.out.println(result);
		
		result %= 4;
		System.out.println(result);
	}

}
